#include "libft.h"

void	*ft_memset(void *ptr, int val, size_t n)
{
	size_t	i;
	char	*p;

	p = (char *)ptr;
	i = 0;
	while (i < n)
	{
		p[i] = val;
		i++;
	}
	return (ptr);
}
